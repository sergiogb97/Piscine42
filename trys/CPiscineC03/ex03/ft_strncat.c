char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int i;

	while (*dest != '\0')
		dest++;
	i = 0;
	while (*src != '\0' && i < nb)
	{
		*dest++ = *src++;
		i++;
	}
	if (*dest != '\0')
		*dest = '\0';
	return (dest);
}
