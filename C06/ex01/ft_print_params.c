#include <unistd.h>

int		main(int argc, char **argv)
{
	int	i;
	int	j;

	i = 1;
	while (argc != 0 && i != argc)
	{
		j = 0;
		while (argv[i][j] != '\0')
			write(1, &argv[i][j++], 1);
		i++;
		write(1, "\n", 1);
	}
	return (0);
}
