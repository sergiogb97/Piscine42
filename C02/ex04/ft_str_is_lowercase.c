int		ft_str_is_lowercase(char *str)
{
	int i;
	int check;

	check = 1;
	i = 0;
	while (*(str + i) != '\0')
	{
		if ('z' < *(str + i) || *(str + i) < 'a')
			check = 0;
		i++;
	}
	return (check);
}
