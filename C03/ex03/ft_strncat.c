char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int i;
	int j;

	i = 0;
	while (dest[i] != '\0')
		i++;
	j = 0;
	while (src[j] != '\0' && nb--)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	if (dest[i] != '\0')
		dest[i] = '\0';
	return (dest);
}
